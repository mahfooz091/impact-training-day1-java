public class relationalOp{
    public static void main(String[] args){

        int a = 24;
        int b = 3;
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(b <= a);
        System.out.println(a >= b);
        System.out.println(a == b);
        System.out.println(a != b);
    }
}